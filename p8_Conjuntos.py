#Conjuntos
#Creacion de conjnuntos
conjunto1 = {1, 2, 3}
conjunto2 = {3, 4, 5}

#haciendo union de los conjuntos creados
union = conjunto1.union(conjunto2)
print(union)

#Haciendo interseccion de los conjuntos creados
interseccion = conjunto1.intersection(conjunto2)
print(interseccion)

#Haciendo la diferencia del conjunto 1 del conjunto 2
diferencia = conjunto1.difference(conjunto2)
print(diferencia)

