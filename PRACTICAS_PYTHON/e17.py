'''
[2:4] significa:
2: Índice de inicio (incluido).
4: Índice de fin (excluido).
Esto toma los caracteres desde el índice 2 hasta el índice 3 (sin incluir el 4)
'''
cadena = 'hola'
subcadena = cadena[2:4]
print(subcadena)