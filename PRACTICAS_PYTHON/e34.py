'''                         EJERCICIO NUMERO #34
            VERIFICA SI UNA CADENA ES MAYOR O IGUAL A 10 CARACTERES.
'''
#len(string) nos permite saber el la cantidad de caracteres de una cadena
cadena = input('Ingresa una palabra: ')
if len(cadena) >= 10:
    print('Tu cadena es mayor igual a 10 caracteres')
else:
    print('Tu cadena es menor de 10 caracteres')